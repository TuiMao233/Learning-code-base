<?php
 /*数据类型,在php中指的是存储的数据本身类型，而本身变量的类型。
   php是一种弱语言，变量本身没有数据类型。*/
  
 /* PHP的八种数据类型
  * 在PHP中将数据分为三大类八小类:
  * 
  * 简单（基本）数据类型：四个小类：
  * 整形：int/integer,系统分配四个字节储存,表示整数类型（有前提）
  * 浮点型：float/double,系统分配八个字节储存，表示小数或者整数存不下的整数
  * 字符串型：string，系统根据实际长度分配，表示字符串（引号）
  * 布尔类型：bool/boolean，表示布尔类型，只有两个值：true和flase
  * 
  * 复合数据类型：
  * 对象类型：object：存放对象（面向对象）
  * 数组类型：array，存储多个数据（一次性）
  * 
  * 特殊数据类型：
  * 资源类型：resource，存放资源数据（php外部数据，如数据库、文件）
  * 空类型：null，只有一个值就是null（不能运算）
 */
?>